def strip_html(html: str) -> str:
    if not html:
        return ""
    soup = BeautifulSoup(html, "html.parser")
    text = soup.get_text(separator=" ").strip()
    # Collapse whitespace
    return " ".join(text.split())


def parse_date(entry) -> datetime:
    """Parse published date from feed entry, return UTC datetime."""
    for attr in ("published_parsed", "updated_parsed"):
        t = getattr(entry, attr, None)
        if t:
            try:
                return datetime(*t[:6], tzinfo=timezone.utc)
            except Exception:
                pass
    return datetime(1970, 1, 1, tzinfo=timezone.utc)


def fetch_topic(topic: str, url: str) -> list[dict]:
    console.print(f"  Fetching [cyan]{topic}[/cyan] ...", end=" ")
    try:
        feed = feedparser.parse(url)
        entries = feed.entries
        console.print(f"[green]{len(entries)} entries[/green]")
    except Exception as e:
        console.print(f"[red]ERROR: {e}[/red]")
        return []

    articles = []
    for entry in entries:
        summary_raw = getattr(entry, "summary", "") or ""
        summary = strip_html(summary_raw)
        # Trim to 300 chars
        if len(summary) > 300:
            summary = summary[:297] + "..."

        pub_dt = parse_date(entry)
        articles.append({
            "topic": topic,
            "title": entry.get("title", "").strip(),
            "url": entry.get("link", "").strip(),
            "summary": summary,
            "published": pub_dt.strftime("%Y-%m-%dT%H:%M:%S"),
            "_pub_dt": pub_dt,
        })

    # Sort descending by date, keep top N
    articles.sort(key=lambda a: a["_pub_dt"], reverse=True)
    return articles[:TOP_N]


def build_readme(articles_by_topic: dict[str, list[dict]], today: str) -> str:
    lines = [
        "# Medium Daily Digest",
        "",
        "Auto-updated daily snapshot of hot articles from Medium across key topics.",
        "",
        "**Topics:** " + ", ".join(TOPICS.keys()),
        "",
        f"## Latest digest: {today}",
        "",
    ]

    for topic, articles in articles_by_topic.items():
        lines.append(f"### {topic}")
        lines.append("")
        lines.append("| Title | Summary | Published |")
        lines.append("|-------|---------|-----------|")
        for a in articles:
            title_link = f"[{a['title']}]({a['url']})"
            summary_snippet = a["summary"][:120].replace("|", "\\|") + ("..." if len(a["summary"]) > 120 else "")
            lines.append(f"| {title_link} | {summary_snippet} | {a['published'][:10]} |")
        lines.append("")

    lines.append("---")
    lines.append(f"*Generated by scraper.py on {today}*")
    lines.append("")
    return "\n".join(lines)


def git_commit_push(today: str):
    json_file = f"output/{today}.json"
    try:
        result = subprocess.run(
            ["git", "add", json_file, "README.md"],
            cwd=Path(__file__).parent,
            capture_output=True, text=True
        )
        if result.returncode != 0:
            console.print(f"[yellow]git add warning:[/yellow] {result.stderr.strip()}")

        result = subprocess.run(
            ["git", "commit", "-m", f"Daily digest {today}"],
            cwd=Path(__file__).parent,
            capture_output=True, text=True
        )
        if result.returncode != 0:
            console.print(f"[yellow]git commit warning:[/yellow] {result.stderr.strip()}")
            return

        console.print(f"[green]Committed:[/green] Daily digest {today}")

        result = subprocess.run(
            ["git", "push", "origin", "main"],
            cwd=Path(__file__).parent,
            capture_output=True, text=True
        )
        if result.returncode != 0:
            console.print(f"[yellow]git push warning:[/yellow] {result.stderr.strip()}")
        else:
            console.print("[green]Pushed to origin/main[/green]")
    except FileNotFoundError:
        console.print("[yellow]git not found — skipping commit/push[/yellow]")


