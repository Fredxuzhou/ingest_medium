def send_email(articles_by_topic: dict[str, list[dict]], today: str):
    html = build_email_html(articles_by_topic, today)
    msg = MIMEMultipart("alternative")
    msg["Subject"] = f"Medium Daily Digest — {today}"
    msg["From"] = EMAIL_FROM
    msg["To"] = EMAIL_TO
    msg.attach(MIMEText(html, "html"))
    try:
        with smtplib.SMTP_SSL("smtp.gmail.com", 465) as server:
            server.login(EMAIL_FROM, EMAIL_APP_PASSWORD)
            server.send_message(msg)
        console.print(f"[bold green]Email sent to {EMAIL_TO}[/bold green]")
    except Exception as e:
        console.print(f"[red]Email failed: {e}[/red]")


def print_table(articles_by_topic: dict[str, list[dict]]):
    table = Table(
        title="Medium Daily Hot Topics",
        box=box.ROUNDED,
        show_lines=True,
        expand=True,
    )
    table.add_column("Topic", style="cyan bold", no_wrap=True, width=12)
    table.add_column("Title", style="white", min_width=30)
    table.add_column("Summary", style="dim", min_width=40)
    table.add_column("Published", style="green", no_wrap=True, width=12)
    table.add_column("URL", style="blue", no_wrap=False, min_width=20)

    for topic, articles in articles_by_topic.items():
        for i, a in enumerate(articles):
            table.add_row(
                topic if i == 0 else "",
                a["title"][:80],
                a["summary"][:120] + ("..." if len(a["summary"]) > 120 else ""),
                a["published"][:10],
                a["url"],
            )

    console.print(table)


def main():
    today = datetime.now().strftime("%Y-%m-%d")
    OUTPUT_DIR.mkdir(exist_ok=True)
    output_file = OUTPUT_DIR / f"{today}.json"

    console.rule(f"[bold cyan]Medium Scraper — {today}[/bold cyan]")

    all_articles: list[dict] = []
    seen_urls: set[str] = set()
    articles_by_topic: dict[str, list[dict]] = {}

    for topic, url in TOPICS.items():
        articles = fetch_topic(topic, url)
        deduped = []
        for a in articles:
            if a["url"] not in seen_urls:
                seen_urls.add(a["url"])
                deduped.append(a)
        articles_by_topic[topic] = deduped
        all_articles.extend(deduped)

    # Strip internal _pub_dt before saving
    save_data = [{k: v for k, v in a.items() if k != "_pub_dt"} for a in all_articles]

    with open(output_file, "w", encoding="utf-8") as f:
        json.dump(save_data, f, indent=2, ensure_ascii=False)
    console.print(f"\n[bold green]Saved {len(save_data)} articles to {output_file}[/bold green]")

    # Update README
    readme_content = build_readme(articles_by_topic, today)
    README_PATH.write_text(readme_content, encoding="utf-8")
    console.print(f"[bold green]Updated {README_PATH}[/bold green]")

    # Terminal table
    console.print()
    print_table(articles_by_topic)

    # Send email digest
    console.print()
    send_email(articles_by_topic, today)

    # Git commit + push
    console.print()
    git_commit_push(today)


if __name__ == "__main__":
    main()
